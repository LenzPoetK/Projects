    private void updateProduct_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateProduct_ButtonActionPerformed
        // TODO add your handling code here:
        
        String selectedItem = (String) roleUpdate.getSelectedItem();
        
        Product p = new Product();
        ProductDAO dao = new ProductDAO();
        
        p.setName(nameUpdate.getText());
        p.setDescription(descriptionUpdate.getText());
        p.setPrice(Double.parseDouble(priceUpdate.getText()));
        p.setRole(selectedItem);
        p.setPrepTime(prepTimeUpdate.getText());
        p.setId((int)jTProducts.getValueAt(jTProducts.getSelectedRow(), 0));

        try {
            dao.update(p);

            nameUpdate.setText("");
            descriptionUpdate.setText("");
            priceUpdate.setText("");
            prepTimeUpdate.setText("");
            roleUpdate.setSelectedIndex(0);

            readJTable();

        } catch (SQLException ex) {
            Logger.getLogger(MainRestaurantView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "An error occurred while saving the product.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateProduct_ButtonActionPerformed

    private void jTProductsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTProductsMouseClicked
        // TODO add your handling code here:
        if (jTProducts.getSelectedRow() != -1) {

            nameUpdate.setText(jTProducts.getValueAt(jTProducts.getSelectedRow(), 1).toString());
            priceUpdate.setText(jTProducts.getValueAt(jTProducts.getSelectedRow(), 2).toString());
            descriptionUpdate.setText(jTProducts.getValueAt(jTProducts.getSelectedRow(), 3).toString());
            roleUpdate.setSelectedItem(jTProducts.getValueAt(jTProducts.getSelectedRow(), 4).toString());
            prepTimeUpdate.setText(jTProducts.getValueAt(jTProducts.getSelectedRow(), 5).toString());

        }
    }//GEN-LAST:event_jTProductsMouseClicked

