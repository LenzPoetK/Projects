    public void readJTable() throws SQLException{
        
        DefaultTableModel model = (DefaultTableModel) jTProducts.getModel();
        model.setNumRows(0);
        ProductDAO pDAO = new ProductDAO();
        
        for(Product p : pDAO.findAll()){
            
            model.addRow(new Object[] {
                p.getId(),
                p.getName(),
                p.getPrice(),
                p.getDescription(),
                p.getRole(),
                p.getPrepTime()
            });
        }
    }

